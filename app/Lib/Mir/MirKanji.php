     public static function replaceMailBreakChars($str, $remove_invalid_chars = false)
     {
        if (! $str) {
            return $str;
        }

        // 安全でない文字を置換
        $replace_chars = MirKanji::replace_chars();
        $needles = array_keys($replace_chars);
        $haystacks = array_values($replace_chars);
        $str = str_replace($needles, $haystacks, $str);

        // 安全でない文字を除去
        if ($remove_invalid_chars) {
            $remove_chars = MirKanji::remove_chars();
            $str = str_replace($remove_chars, '', $str);
        }

        // K    「半角カタカナ」を「全角カタカナ」に変換します。
        // n	「全角」数字を「半角」に変換します。
        $str = mb_convert_kana($str, 'nK');

        return $str;
    }

